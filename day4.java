package PenggunaanEscapeSequence;

public class day4 {
    
    public static void main(String[] args) {
        // Penggunaan \n = newline (baris baru/pindah baris)
        System.out.println("Hello, teman-teman!\nBerikut biodata saya:");
        
        //Penggunaan \t = tab (spasi lebar)
        System.out.println("Nama:\tNur Alim");
        System.out.println("Umur:\t17 tahun");
        System.out.println("Alamat:\tTeppo");
        
        //Penggunaan \" = Menampilkan tanda kutip ganda
        System.out.println("Saya akrab dipanggil \"Alim\"");
        
        //Penggunaan \\ = Menampikan backslash (\)
        System.out.println("Lokasi file: Penyimpanan di Perangkat\\100 Days Of Coding");
        
        //Penggunaan \' = Menampilkan tanda kutip tunggal
        System.out.println("Huruf awal nama saya: \'N\'");
        
    }
    
}
